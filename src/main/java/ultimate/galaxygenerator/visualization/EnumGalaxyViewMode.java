package ultimate.galaxygenerator.visualization;

/**
 * Enum for specifying the view onto a galaxy for rendering in within a {@link GalaxyViewFrame}.
 * 
 * @author ultimate
 */
public enum EnumGalaxyViewMode
{
	/**
	 * view onto the y-z-plane
	 */
	yz, 
	/**
	 * view onto the x-z-plane
	 */
	xz,
	/**
	 * view onto the x-y-plane
	 */
	xy;
}
